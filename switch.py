dia = int(input("Ingresa un número del uno al cinco: "))
if dia == 1:
	print("Lunes")
elif dia == 2:
	print("Martes")
elif dia == 3:
	print("Miercoles")
elif dia == 4:
	print("Jueves")
elif dia == 5:
	print("Viernes")
else:                          #Primera forma de colocar la opción
	print("Opción lo válida")  #"Default" en una selección múltiple
print("Fin del programa")